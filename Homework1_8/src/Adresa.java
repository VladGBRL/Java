public class Adresa {
    private String strada;
    private String oras;
    private String tara;

    public Adresa(String strada, String oras, String tara) {
        this.strada = strada;
        this.oras = oras;
        this.tara = tara;
    }

    public String getStrada() {
        return strada;
    }

    public String getOras() {
        return oras;
    }
}
